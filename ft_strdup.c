#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*str;
	unsigned long	len;
	unsigned long	i;

	str = NULL;
	len = 0;
	while (s1[len])
		len++;
	str = malloc(sizeof(char) * (len + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
